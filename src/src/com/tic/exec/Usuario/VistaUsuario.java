    private void refrescarTabla() {
        DefaultTableModel m = (DefaultTableModel) this.jTableUsuario.getModel();
        m.setNumRows(0);

        Incidencia i = null;
        try {
            ArrayList<Incidencia> ai = (ArrayList<Incidencia>) udi.getIncidenciasUsuario(1);
            for (int j = 0; j < ai.size(); j++) {
                i = ai.get(j);
                Object[] o = {
                    i.getIdIncidencia(),
                    i.getEstado(),
                    i.getResultado_cierre(),
                    i.getFechaCierre(),
                    i.getFechaEntrada(),
                    i.getTipoIncidencia(),
                    i.getIdUsuario(),
                    i.getIdTecnico(),
                    i.getDescripcionIncidencia(),
                    i.getDescripcionSolucion()
                };
                m.addRow(o);
            }
        } catch (Exception e) {
            System.out.println("Error al mostrar tabla incidencia");
            e.printStackTrace();
        }
    }

    private void refrescarTablaPorID(int id) {
        DefaultTableModel m = (DefaultTableModel) this.jTableUsuario.getModel();
        m.setNumRows(0);

        Incidencia i = null;
        try {
            i = udi.getIncidenciaPorId(id);

            Object[] o = {
                i.getIdIncidencia(),
                i.getEstado(),
                i.getResultado_cierre(),
                i.getFechaCierre(),
                i.getFechaEntrada(),
                i.getTipoIncidencia(),
                i.getIdUsuario(),
                i.getIdTecnico(),
                i.getDescripcionIncidencia(),
                i.getDescripcionSolucion()
            };

            m.addRow(o);
        } catch (Exception ex) {
            System.out.println("Error al cargar la tabla por id");
        }
    }
