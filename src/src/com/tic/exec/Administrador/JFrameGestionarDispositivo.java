    public void refrescarTabla() {
        DefaultTableModel m = (DefaultTableModel) this.jTableDispositivo.getModel();
        m.setNumRows(0);

        Dispositivo i = null;
        try {
            ArrayList<Dispositivo> ai = (ArrayList<Dispositivo>) adminDAO.getDispositivos();
            for (int j = 0; j < ai.size(); j++) {
                i = ai.get(j);
                Object[] o = {
                    i.getIdDispositivo(),
                    i.getIdEspacio(),
                    i.getTipo(),
                    i.getDescripcion(),
                    i.getMarca(),
                    i.getModelo()
                };
                m.addRow(o);
            }
        } catch (Exception e) {
            System.out.println("Error al mostrar tabla incidencia");
            e.printStackTrace();
        }
    }

    public void refrescarTablaTipo(String tipo) {
        DefaultTableModel m = (DefaultTableModel) this.jTableDispositivo.getModel();
        m.setNumRows(0);

        Dispositivo i = null;
        try {
            ArrayList<Dispositivo> ai = (ArrayList<Dispositivo>) adminDAO.getDispositivos();
            for (int j = 0; j < ai.size(); j++) {
                i = ai.get(j);
                if (i.getTipo().equals(tipo)) {
                    Object[] o = {
                        i.getIdDispositivo(),
                        i.getIdEspacio(),
                        i.getTipo(),
                        i.getDescripcion(),
                        i.getMarca(),
                        i.getModelo()
                    };
                    m.addRow(o);
                }

            }
        } catch (Exception e) {
            System.out.println("Error al mostrar tabla incidencia");
            e.printStackTrace();
        }
    }

